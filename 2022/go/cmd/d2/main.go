func parseRounds(line string) (int, error) {
	parts := strings.Split(line, " ")

	if parts[0] == "A" {
		if parts[1] == "X" {
			return 3 + 0, nil
		}
		if parts[1] == "Y" {
			return 1 + 3, nil
		}
		if parts[1] == "Z" {
			return 2 + 6, nil
		}
	}
	if parts[0] == "B" {
		if parts[1] == "X" {
			return 1 + 0, nil
		}
		if parts[1] == "Y" {
			return 2 + 3, nil
		}
		if parts[1] == "Z" {
			return 3 + 6, nil
		}
	}
	if parts[0] == "C" {
		if parts[1] == "X" {
			return 2 + 0, nil
		}
		if parts[1] == "Y" {
			return 3 + 3, nil
		}
		if parts[1] == "Z" {
			return 1 + 6, nil
		}
	}
	return -1, errors.New("Could not determine winner in round")
}

// Rock = A = 1, Paper = B = 2, Scissors = C = 3
// X = lose, Y = draw, Z = win

func main() {
	rounds := strings.Split(getInput(), "\n")
	totalScore := 0
	for _, round := range rounds {
		roundScore, err := parseRounds(round)
		if err != nil {
			fmt.Println(err)
		}

		totalScore += roundScore
	}

	fmt.Println(totalScore)
}
